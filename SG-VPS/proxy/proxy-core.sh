_ui_state_text() {
  case "$1" in
    "已开启")
      printf '%b' "${C_GREEN}${C_BOLD}● 已开启${C_RESET}  ${C_GREEN}当前代理已完整启用${C_RESET}"
      ;;
    "未开启")
      printf '%b' "${C_RED}${C_BOLD}● 未开启${C_RESET} ${C_DIM}当前代理未启用${C_RESET}"
      ;;
    *)
      printf '%b' "${C_YELLOW}${C_BOLD}● 部分开启${C_RESET} ${C_YELLOW}有模块开启，或代理源不一致${C_RESET}"
      ;;
  esac
}

_ui_port_text() {
  local addr="$1"
  if _proxy_port_open "$addr"; then
    printf '%b' "${C_GREEN}● 可连接${C_RESET}"
  else
    printf '%b' "${C_RED}● 不可连接${C_RESET}"
  fi
}

_ui_module_text() {
  local value="$1"
  local current="$2"

  if [ -z "$value" ]; then
    printf '%b' "${C_RED}● 关闭${C_RESET}"
  elif [ "$value" = "$current" ]; then
    printf '%b' "${C_GREEN}● 开启${C_RESET} ${C_DIM}${value}${C_RESET}"
  else
    printf '%b' "${C_YELLOW}● 其他源${C_RESET} ${C_DIM}${value}${C_RESET}"
  fi
}

_ui_kv() {
  local key="$1"
  local value="$2"
  printf '  %b%-16s%b %b\n' "${C_DIM}" "$key" "${C_RESET}" "$value"
}

_ui_source_row() {
  local index="$1" key="$2" name="$3" addr="$4" role
  if [ "$PROXY_DEFAULT" = "$key" ]; then
    role="${C_GREEN}${C_BOLD}当前使用${C_RESET}"
  else
    role="${C_DIM}备用${C_RESET}"
  fi
  printf '  %b[%s]%b %-14s %b%-22s%b %-14b %b\n' \
    "${C_CYAN}" "$index" "${C_RESET}" \
    "$name" \
    "${C_MAGENTA}" "$addr" "${C_RESET}" \
    "$(_ui_port_text "$addr")" \
    "$role"
}

_ui_module_row() {
  local name="$1" value="$2" current="$3"
  printf '  %-18s %b\n' "$name" "$(_ui_module_text "$value" "$current")"
}

_proxy_status() {
  _proxy_load_conf

  local provider addr http socks
  local shell_v profile_v apt_v git_v wget_v docker_conf_v docker_run_v bt_v
  local summary

  provider="$(_proxy_provider_label)"
  addr="$(_proxy_provider_addr)"
  http="$(_proxy_http_url)"
  socks="$(_proxy_socks_url)"

  shell_v="$(_proxy_shell_value)"
  profile_v="$(_proxy_profile_value)"
  apt_v="$(_proxy_apt_value)"
  git_v="$(_proxy_git_value)"
  wget_v="$(_proxy_wget_value)"
  docker_conf_v="$(_proxy_docker_config_value)"
  docker_run_v="$(_proxy_docker_runtime_value)"
  bt_v="$(_proxy_bt_value)"

  summary="$(_proxy_summary_state "$http" "$shell_v" "$profile_v" "$apt_v" "$git_v" "$wget_v")"

  _ui_header
  _ui_title "当前状态"
  _ui_kv "总体状态" "$(_ui_state_text "$summary")"
  _ui_kv "默认代理源" "${C_MAGENTA}${C_BOLD}${provider}${C_RESET}"
  _ui_kv "HTTP 代理" "${C_GREEN}${http}${C_RESET}"
  _ui_kv "SOCKS 代理" "${C_GREEN}${socks}${C_RESET}"
  _ui_kv "当前端口" "${C_MAGENTA}${addr}${C_RESET}  $(_ui_port_text "$addr")"
  _ui_kv "直连规则" "${C_DIM}局域网 / Docker / WireGuard / EasyTier / 本地域名 / 自定义域名${C_RESET}"

  _ui_title "代理源"
  _ui_source_row "1" "mobaxterm" "MobaXterm" "$PROXY_MOBAXTERM"
  _ui_source_row "2" "mihomo" "VPS mihomo" "$PROXY_MIHOMO"

  _ui_title "模块状态"
  _ui_module_row "当前终端" "$shell_v" "$http"
  _ui_module_row "持久终端" "$profile_v" "$http"
  _ui_module_row "APT" "$apt_v" "$http"
  _ui_module_row "Git" "$git_v" "$http"
  _ui_module_row "Wget" "$wget_v" "$http"
  _ui_module_row "Docker 配置" "$docker_conf_v" "$http"
  _ui_module_row "Docker 运行时" "$docker_run_v" "$http"
  _ui_module_row "BT 面板进程" "$bt_v" "$http"

  _ui_title "快捷命令"
  printf '  %bproxy-on%b      %bproxy-off%b      %bproxy-status%b\n' \
    "${C_GREEN}" "${C_RESET}" \
    "${C_RED}" "${C_RESET}" \
    "${C_BLUE}" "${C_RESET}"
  printf '  %bproxy-use mobaxterm%b      %bproxy-use mihomo%b      %bdocker-proxy-restart%b\n' \
    "${C_MAGENTA}" "${C_RESET}" \
    "${C_MAGENTA}" "${C_RESET}" \
    "${C_YELLOW}" "${C_RESET}"
  printf '  %bNO_COLOR=1 proxy%b\n' "${C_DIM}" "${C_RESET}"
}

