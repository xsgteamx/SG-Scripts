_proxy_switch_menu() {
  _proxy_load_conf
  _ui_header
  _ui_title "切换代理源"
  _ui_kv "当前默认源" "${C_MAGENTA}${C_BOLD}${PROXY_DEFAULT}${C_RESET}"
  echo
  _ui_source_row "1" "mobaxterm" "MobaXterm" "$PROXY_MOBAXTERM"
  _ui_source_row "2" "mihomo" "VPS mihomo" "$PROXY_MIHOMO"
  echo

  local c
  read -r -p "请选择默认代理源 > " c

  case "$c" in
    1) _proxy_use mobaxterm ;;
    2) _proxy_use mihomo ;;
    *) echo "已取消。"; return 0 ;;
  esac

  echo
  read -r -p "是否立刻按新源重新开启代理？[y/N] " c
  case "$(_proxy_lc "$c")" in
    y|yes) _proxy_on ;;
  esac
}

_proxy_pause() {
  [ -t 0 ] || return 0
  echo
  read -r -p "按回车返回控制台..." _
}

_proxy_menu() {
  while true; do
    if [ -t 1 ] && command -v clear >/dev/null 2>&1; then
      clear
    fi

    _proxy_status
    _proxy_menu_panel
    echo

    local choice
    read -r -p "请选择 > " choice

    case "$choice" in
      1) _proxy_on; _proxy_pause ;;
      2) _proxy_off; _proxy_pause ;;
      3) _proxy_switch_menu; _proxy_pause ;;
      4) _proxy_docker_restart; _proxy_pause ;;
      5) _proxy_restart_bt; _proxy_pause ;;
      6) continue ;;
